package week3.learningvariables;

public class Identifiers {

	public static void main(String[] args) {
//		int 1stAge; Cannot start with number
//		int static; Cannot be a reserve word
//		int @name; Cannot be other symbol than _ and $
		int studentAge;
		studentAge = 20;
		System.out.println(studentAge);
		int student2Age;
		studentAge = 23;
		System.out.println(studentAge);
		student2Age = 30;
		System.out.println(student2Age);
		student2Age = 40;
		System.out.println(studentAge);
		System.out.println(student2Age);
	}
	
}
